## Para tratar sring com aspas
mensagem = '"Yana do dev" é uma ótima engenheira de dados!'
print(mensagem)

## Para tratar sring com aspas simples
mensagem1 = "Yana do 'dev' é uma ótima engenheira de dados!"
print(mensagem1)



## Para poder utilizar aspas simples e duplas no conteúdo da string.
mensagem3 = """ 'Yana do dev' é uma ótima engenheira de dados"!"""
print(mensagem3)


## Para poder  declarar uma string que necessita de várias linhas.
mensagem4 = """ 'Yana do dev' 
é uma ótima 
engenheira de dados"!"""

print(mensagem4)


